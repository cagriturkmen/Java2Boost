package com.bilgeadam.boost.lesson008;

public class IfElse {

	public static void main(String[] args) {
		
		int x,y;
		x=6;
		y=2;
		
		if(x<y) {
			System.out.println("x is smaller than y.");
		}else if(x==y) {
			System.out.println("x is equal to y.");
		}
		else {
			System.out.println("x is greater than y.");
		}
		
		
	}

}
